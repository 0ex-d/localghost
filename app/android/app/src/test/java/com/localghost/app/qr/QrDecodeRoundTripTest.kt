    @Test
    fun decodesRotatedGrid() {
        // A camera holds the QR at any angle; decode must recover from any of the four orientations.
        val g = grid(helloV1M)
        val n = g.size
        val rotated = Array(n) { r -> BooleanArray(n) { c -> g[n - 1 - c][r] } }
        assertEquals("hello", QrMatrixDecode.decode(rotated))
    }

